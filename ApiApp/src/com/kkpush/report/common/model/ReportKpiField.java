package com.kkpush.report.common.model;

public class ReportKpiField {

	private String kpiCode;

	private Integer date;

	private String kpiField;

	private String kpiValue;

	/**
	 * @return the kpiCode
	 */
	public String getKpiCode() {
		return kpiCode;
	}

	/**
	 * @param kpiCode the kpiCode to set
	 */
	public void setKpiCode(String kpiCode) {
		this.kpiCode = kpiCode;
	}

	/**
	 * @return the date
	 */
	public Integer getDate() {
		return date;
	}

	/**
	 * @param date the date to set
	 */
	public void setDate(Integer date) {
		this.date = date;
	}

	/**
	 * @return the kpiField
	 */
	public String getKpiField() {
		return kpiField;
	}

	/**
	 * @param kpiField the kpiField to set
	 */
	public void setKpiField(String kpiField) {
		this.kpiField = kpiField;
	}

	/**
	 * @return the kpiValue
	 */
	public String getKpiValue() {
		return kpiValue;
	}

	/**
	 * @param kpiValue the kpiValue to set
	 */
	public void setKpiValue(String kpiValue) {
		this.kpiValue = kpiValue;
	}

}
